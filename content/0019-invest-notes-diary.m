资本市场就是一个大赌场，里面有无数的局，但是人人都往里钻，人人都想捞一把。

玩股票和基金也有6，7年了。总的来说，小赚大亏。就是赚的时候，常常赚一点点，亏的时候，常常亏很多。 这种情况，跟进澳门赌场何其类似！ 尽管这样，人们还是乐此不疲，这就是生活，这就是人生！

<!-- $ -->

话虽如此，但是跟纯粹的赌场还是有区别的。这些区别，善加利用者，将一荣俱荣；否则一损俱损。

